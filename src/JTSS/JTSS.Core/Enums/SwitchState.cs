namespace JTSS.Core.Enums
{
    /// <summary>
    /// Represents the state of a switch node.
    /// </summary>
    public enum SwitchState
    {
        /// <summary>
        /// The switch is in the normal position (facing to trailing).
        /// </summary>
        Normal,
        /// <summary>
        /// The switch is in the reversed position (facing to diverging).
        /// </summary>
        Reversed
    }
}